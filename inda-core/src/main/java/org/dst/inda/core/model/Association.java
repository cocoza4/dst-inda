package org.dst.inda.core.model;

public class Association {
	
	private String artfId;
	private String artfTitle;
	private String createdBy;
	private String description;
	
	public Association() {}
	
	public String getArtfId() {
		return artfId;
	}
	
	public void setArtfId(String artfId) {
		this.artfId = artfId;
	}
	
	public String getArtfTitle() {
		return artfTitle;
	}
	
	public void setArtfTitle(String artfTitle) {
		this.artfTitle = artfTitle;
	}
	
	public String getCreatedBy() {
		return createdBy;
	}
	
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription(String description) {
		this.description = description;
	}

}
